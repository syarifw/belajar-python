# - Apa itu Docstrings ?
# Docstrings adalah penjelasan yang akan muncul dalam pop up message di IDE setelah kita
# menambahkan function atau method tertentu, khususnya yang memiliki nilai balikan

# - Dimana kita hendaknya menulis Docstrings?
# Letaknya biasanya ada di baris pertama sebuah fungsi setelah fungsi tersebut dituliskan

# - Bagaimana cara menulisnya ?
# Dengan menggunakan tiga tanda petik dua (layaknya menggunakan multiline comment/string)
# Ex : 
# def contoh(format):
# """  Fungsi ini akan mengembalikan nilai integer """